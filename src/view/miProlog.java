    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:     ---------------- importar un archivo txt
        JFileChooser fileChooser = new JFileChooser();

        FileNameExtensionFilter filter1 = new FileNameExtensionFilter("Archivos de texto", "txt");
        FileNameExtensionFilter filter2 = new FileNameExtensionFilter("Archivos de Prolog", "pl");
        fileChooser.setFileFilter(filter1);
        fileChooser.setFileFilter(filter2);

        JOptionPane.showMessageDialog(null, "procure seleccionar un archivo en formato .txt o .pl");
        int seleccion = fileChooser.showOpenDialog(jTextArea1);

        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File fichero = fileChooser.getSelectedFile();
            if (fichero.getName().endsWith("txt") || fichero.getName().endsWith(".pl")) {
                // Aquí debemos abrir y leer el fichero.
                try ( Reader r = new FileReader(fichero.getPath());  BufferedReader br = new BufferedReader(r)) {
                    String lineaEnTurno;
                    String textoEnteroDelTxt = "";

                    BaseDeConocimiento baseDeConocimientoDelTxt = new BaseDeConocimiento();
                    do {
                        lineaEnTurno = br.readLine();

                        if (lineaEnTurno != null) {
                            baseDeConocimientoDelTxt.Agregar(new clausula(lineaEnTurno));
                            textoEnteroDelTxt += lineaEnTurno + '\n';
                        }
                    } while (lineaEnTurno != null);

                    jTextArea1.setText(textoEnteroDelTxt);
                    this.baseDeConocimiento = baseDeConocimientoDelTxt;
                    JOptionPane.showMessageDialog(null, """
                                                        se cargo a la base de Conocimiento ! 
                                                         procede a introducir un goal :) !""");

                } catch (Exception e) {
                    System.out.println(e);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Seleccionar formato Valido");
            }

        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

