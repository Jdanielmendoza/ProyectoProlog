        String nombreDeComoSeGuardaraElArchivo = "BaseDeConocimiento".concat(horaActualTexto).concat(".pl");
        fileChooser.setSelectedFile(new File(nombreDeComoSeGuardaraElArchivo));

        int seleccion = fileChooser.showSaveDialog(jTextArea1);

